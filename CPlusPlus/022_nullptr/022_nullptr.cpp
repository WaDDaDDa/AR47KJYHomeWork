#include <iostream>

int main()
{

	int Value = 10;

	// 포인터를 안쓸수도있다.
	// int포인터면 어떤 정수의 주소값을 받겠다는것.
	// 안들고있을수있다.
	// 그것을 표현하기위한 상수가.

	// nullptr == 0이라고 부릅니다.
	// 약속을했다. 0번지는 nullptr로 하기로.
	// 0번지에는 nullptr이 있는것이다. 0번지는 임의로 사용할수 없게 정해 놓은것.
	// 주소를 아직 아무도 가리키고 있지않다.
	int* Ptr = nullptr;

	// 포인터의 초기화 방법 이라고도 할수있다.

	// 사용하지 않는다는걸 표현할수 있다는건 기능이 다재다능 하다.
	// 날 쓰면 안된다는 의미를 내포할수 있다 라는 의미이다.
	Value = 10;
	Ptr = &Value; // nullptr이던 포인터Ptr을 Value의 주소값으로 재지정
	*Ptr = 200;  // Value값에 200을 넣는다.

}
